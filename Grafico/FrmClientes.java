    private void jButtonNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNewActionPerformed

        try {
            connBairro.executaSQL("select * from bairro where nome_bairro = '"
                    + jComboBoxBairro.getSelectedItem() + "'");
            connBairro.rs.first();
            mod.setBairro(connBairro.rs.getInt("id_bairro"));
            connTel.executaSQL("select * from telefone where numero_tel = '"
                    + jComboBoxTel.getSelectedItem() + "'");
            connTel.rs.first();
            mod.setTel(connTel.rs.getInt("id_telefone"));
            connCidade.executaSQL("select * from cidade where nome_cidade = '" + jComboBoxCidade.getSelectedItem() + "'");
            connCidade.rs.first();
            mod.setCidade(connCidade.rs.getInt("id_cidade"));
            mod.setNome(jTextFieldNome.getText());
            mod.setEndereco(jTextFieldEndereco.getText());
            mod.setCpf(jFormattedTextFieldCPF.getText());
            mod.setRg(jFormattedTextFieldRG.getText());
            control.novo(mod);
            clear();
            inicial();
            jTable = control.preencherTabela(jTable);
        } catch (SQLException ex) {
            Logger.getLogger(FrmClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonNewActionPerformed

    private void jButtonFirstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFirstActionPerformed
        String sigla, cidade, bairro;
        connCliente.executaSQL("select * from clientes order by id_cliente");
        try {
            if (connCliente.rs.first()) {
                jTextFieldCod.setText(String.valueOf(connCliente.rs.getInt("id_cliente")));
                jTextFieldNome.setText(connCliente.rs.getString("nome_cliente"));
                jTextFieldEndereco.setText(connCliente.rs.getString("endereco"));
                jFormattedTextFieldCPF.setText(connCliente.rs.getString("cpf_cliente"));
                jFormattedTextFieldRG.setText(connCliente.rs.getString("rg_cliente"));
                connTel.executaSQL("select * from telefone where id_telefone = " + connCliente.rs.getInt("id_telefone"));
                connTel.rs.first();
                jComboBoxTel.setSelectedItem(connTel.rs.getString("numero_tel"));
                connBairro.executaSQL("select * from bairro inner join cidade on"
                        + " bairro.id_cidade = cidade.id_cidade "
                        + "inner join estados on cidade.id_estado = estados.id_estado and id_bairro = " + connCliente.rs.getInt("id_bairro"));
                connBairro.rs.first();
                sigla = connBairro.rs.getString("sigla_estado");
                cidade = connBairro.rs.getString("nome_cidade");
                bairro = connBairro.rs.getString("nome_bairro");
                jComboBoxEstado.setSelectedItem(sigla);
                jComboBoxCidade = control.preencheComboCidade(jComboBoxCidade, sigla);
                jComboBoxCidade.setSelectedItem(cidade);
                jComboBoxBairro = control.preencheComboBairro(jComboBoxBairro, cidade);
                jComboBoxBairro.setSelectedItem(bairro);
                selecao();
                jButtonPrevious.setEnabled(false);
                if (connCliente.rs.next()) {
                    connCliente.rs.previous();
                    jButtonNext.setEnabled(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Não existem clientes cadastrados");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonFirstActionPerformed

