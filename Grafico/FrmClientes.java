    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed

        try {
            connBairro.executaSQL("select * from bairro inner join cidade on bairro.id_cidade = cidade.id_cidade "
                    + "inner join estados on cidade.id_estado = estados.id_estado and nome_bairro = '" + jComboBoxBairro.getSelectedItem() + "'");
            connBairro.rs.first();
            connTel.executaSQL("select * from telefone where numero_tel = '" + jComboBoxTel.getSelectedItem() + "'");
            connTel.rs.first();
            mod.setCod(Integer.parseInt(jTextFieldCod.getText()));
            mod.setNome(jTextFieldNome.getText());
            mod.setEndereco(jTextFieldEndereco.getText());
            mod.setCpf(jFormattedTextFieldCPF.getText());
            mod.setRg(jFormattedTextFieldRG.getText());
            mod.setTel(connTel.rs.getInt("id_telefone"));
            mod.setBairro(connBairro.rs.getInt("id_bairro"));
            mod.setCidade(connBairro.rs.getInt("id_cidade"));
            control.edita(mod);
            jTable = control.preencherTabela(jTable);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Falha na alteração dos dados!" + ex);
        }
        inicial();
        clear();
    }//GEN-LAST:event_jButtonSaveActionPerformed

