    private void jButtonFirstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFirstActionPerformed
        try {
            connBairro.executaSQL("select * from bairro order by id_bairro");
            connBairro.rs.first();
            jTextFieldCod.setText(String.valueOf(connBairro.rs.getInt("id_bairro")));
            jTextFieldBairro.setText(connBairro.rs.getString("nome_bairro"));
            connCidade.executaSQL("select * from cidade inner join estados on cidade.id_estado = estados.id_estado and id_cidade =" + connBairro.rs.getInt("id_cidade"));
            connCidade.rs.first();
            jComboBoxSigla.setSelectedItem(connCidade.rs.getString("sigla_estado"));
            jComboBoxCidade = control.preencheCidades(jComboBoxCidade, (String) jComboBoxSigla.getSelectedItem());
            jComboBoxCidade.setSelectedItem(connCidade.rs.getString("nome_cidade"));
            view_mode();
        } catch (SQLException ex) {
            Logger.getLogger(ControleBairro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonFirstActionPerformed

